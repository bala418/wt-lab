package exp1;

public class q6 {
    public static void main(String[] args) {
        // print even numbers from 1 to 50
        System.out.println("Even numbers from 1 to 50 are:");
        for (int i = 1; i <= 50; i++) {
            if (i % 2 == 0) {
                System.out.print(i + " ");
            }
        }
    }
}
